#include<iostream>
using namespace std;

int main(){
    // integer
    int a;
    cout<<"Size of int "<<sizeof(a)<<endl;
    // float
    float b;
    cout<<"Size of float "<<sizeof(b)<<endl;
    // char
    char c;
    cout<<"Size of char "<<sizeof(c)<<endl;
    // bool
    bool d;
    cout<<"Size of bool "<<sizeof(d)<<endl;
    // short int
    short int e;
    cout<<"Size of short int "<<sizeof(e)<<endl;
    // long int
    long int f;
    cout<<"Size of long int "<<sizeof(f)<<endl;
}
// signed - has both +/- integers
// unsigned - has only + integers
// long - stores in 4 bytes in memory and has long range
// short - stores in 2 bytes in memory and has short range